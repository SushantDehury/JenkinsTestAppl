package com.nt.bo;

public class AircraftBO {
	private int pid;
	private String name;
	private String origin;
	private String type;
	private  int tolal;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getTolal() {
		return tolal;
	}
	public void setTolal(int tolal) {
		this.tolal = tolal;
	}
	

}
